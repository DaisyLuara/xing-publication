<template>
  <div class="logout">
    <div
      class="logo-wrap">
      <div
        class="logo">
        <img
          src="../../assets/images/logo.png">
      </div>
    </div>
    <el-popover
      ref="popover"
      v-model="visible"
      placement="left"
      width="80"
      trigger="hover"
      popper-class="popper-logout">
      <span 
        class="logout-btn"
        @click="logout">登出</span>
    </el-popover>
    <div 
      v-popover:popover
      class="avatar-wrap" 
      @click="handleUser">
      <span>{{ name }}</span>
      <div class="avatar-block">
        <img 
          src="~assets/images/user-default-icon.png" 
          alt="" 
          class="avatar">
      </div>
    </div>
  </div>
</template>
<script>
import { Popover } from 'element-ui'
import auth from 'service/auth'

export default {
  components: {
    'el-popover': Popover
  },
  data() {
    return {
      visible: false,
      name: null
    }
  },
  created() {
    let user_info = JSON.parse(this.$cookie.get('user_info'))
    this.name = user_info.name
  },
  methods: {
    logout() {
      this.visible = false
      auth.logout(this)
    },
    handleUser() {
      this.$router.push({
        path: '/account/account'
      })
    }
  }
}
</script>

